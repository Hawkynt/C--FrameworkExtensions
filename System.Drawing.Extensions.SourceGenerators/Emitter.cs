using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

namespace Hawkynt.ColorProcessing.SourceGenerators;

/// <summary>
/// Produces the partial-class source files that feed each registry with compile-time
/// descriptor entries.
/// </summary>
/// <remarks>
/// <para>
/// One method per registry so the emitter stays explicit and easy to extend. All emitted
/// code uses <c>typeof(T)</c> and <c>new T()</c> — no reflection or <c>Activator</c>, so
/// trim/AOT analyzers see everything they need.
/// </para>
/// </remarks>
internal static class Emitter {

  private const string Header =
    "// <auto-generated by System.Drawing.Extensions.SourceGenerators/>\n" +
    "// Do not edit — changes will be lost on next build.\n" +
    "#nullable enable\n";

  public static void EmitScalers(SourceProductionContext ctx, ImmutableArray<RegistryCandidate> items) {
    var ordered = _Order(items);
    var sb = new StringBuilder(Header);
    sb.AppendLine("using System.Collections.Generic;");
    sb.AppendLine("using Hawkynt.ColorProcessing.Resizing;");
    sb.AppendLine();
    sb.AppendLine("namespace Hawkynt.ColorProcessing.Resizing;");
    sb.AppendLine();
    sb.AppendLine("partial class ScalerRegistry {");
    sb.AppendLine("  static partial void _CollectFromSourceGenerator(global::System.Collections.Generic.List<global::Hawkynt.ColorProcessing.Resizing.ScalerDescriptor> into) {");

    foreach (var c in ordered) {
      // Skip types decorated with [ScalerInfo] that do not implement one of the two scaler
      // interfaces (e.g. IDownscaler-only types). Matches the runtime-reflection filter in
      // ScalerDescriptor.FromType which returns null for them.
      if (!c.ImplementsIRescaler && !c.ImplementsIResampler)
        continue;

      var typeRef = c.FullyQualifiedTypeName;
      var args = _ResolveScalerAttributeArgs(c);
      var supportedScales = c.HasSupportedScalesStaticProperty ? $"{typeRef}.SupportedScales" : "global::System.Array.Empty<global::Hawkynt.ColorProcessing.Resizing.ScaleFactor>()";
      var isRescaler = c.ImplementsIRescaler ? "true" : "false";
      var isResampler = c.ImplementsIResampler ? "true" : "false";
      sb.Append("    into.Add(global::Hawkynt.ColorProcessing.Resizing.ScalerDescriptor.__CreateFromGenerator(");
      sb.Append("typeof(").Append(typeRef).Append("), ");
      sb.Append(args.Name).Append(", ");
      sb.Append(args.Author).Append(", ");
      sb.Append(args.Description).Append(", ");
      sb.Append(args.Url).Append(", ");
      sb.Append(args.Year).Append(", ");
      sb.Append(args.Category).Append(", ");
      sb.Append(supportedScales).Append(", ");
      sb.Append(isRescaler).Append(", ");
      sb.Append(isResampler).Append(", ");
      // Default-instance radius for resamplers — compile-time branched, zero reflection.
      if (c.ImplementsIResampler) {
        sb.Append("((global::Hawkynt.ColorProcessing.Resizing.IResampler)(object)new ").Append(typeRef).Append("()).Radius");
      } else {
        sb.Append("0");
      }
      sb.AppendLine("));");
    }

    sb.AppendLine("  }");
    sb.AppendLine("}");
    ctx.AddSource("ScalerRegistry.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
  }

  public static void EmitFilters(SourceProductionContext ctx, ImmutableArray<RegistryCandidate> items) {
