  }

  public static void EmitDitherers(SourceProductionContext ctx, ImmutableArray<RegistryCandidate> items) {
    var ordered = _Order(items);
    var sb = new StringBuilder(Header);
    sb.AppendLine("using System;");
    sb.AppendLine("using System.Collections.Generic;");
    sb.AppendLine("using Hawkynt.ColorProcessing;");
    sb.AppendLine("using Hawkynt.ColorProcessing.Dithering;");
    sb.AppendLine();
    sb.AppendLine("namespace Hawkynt.ColorProcessing.Dithering;");
    sb.AppendLine();
    sb.AppendLine("partial class DithererRegistry {");
    sb.AppendLine("  static partial void _CollectFromSourceGenerator(global::System.Collections.Generic.List<global::Hawkynt.ColorProcessing.Dithering.DithererDescriptor> into) {");

    foreach (var c in ordered) {
      if (!c.ImplementsIDitherer)
        continue;

      var typeRef = c.FullyQualifiedTypeName;
      var args = _ResolveDithererAttributeArgs(c);
      // Clean type name (strip "Ditherer" suffix).
      var simpleName = _SimpleTypeName(typeRef);
      var typeName = simpleName.EndsWith("Ditherer") ? simpleName.Substring(0, simpleName.Length - 8) : simpleName;

      if (c.HasStaticIDithererProperties) {
        foreach (var propName in c.StaticIDithererPropertyNames) {
          var name = RegistrySourceGenerator.EscapeStringLiteral($"{typeName}_{propName}");
          sb.Append("    into.Add(global::Hawkynt.ColorProcessing.Dithering.DithererDescriptor.__CreateFromGenerator(");
          sb.Append("typeof(").Append(typeRef).Append("), ");
          sb.Append(name).Append(", ");
          sb.Append(args.Author).Append(", ");
          sb.Append(args.Description).Append(", ");
          sb.Append(args.Type).Append(", ");
          sb.Append(args.Year).Append(", ");
          sb.Append("static () => (global::Hawkynt.ColorProcessing.IDitherer)").Append(typeRef).Append(".").Append(propName);
          sb.AppendLine("));");
        }
      } else if (c.HasParameterlessCtor) {
        var nameExpr = args.NameRaw is { Length: > 0 }
          ? args.Name
          : RegistrySourceGenerator.EscapeStringLiteral(simpleName.Replace("Ditherer", string.Empty));
        sb.Append("    into.Add(global::Hawkynt.ColorProcessing.Dithering.DithererDescriptor.__CreateFromGenerator(");
        sb.Append("typeof(").Append(typeRef).Append("), ");
        sb.Append(nameExpr).Append(", ");
        sb.Append(args.Author).Append(", ");
        sb.Append(args.Description).Append(", ");
        sb.Append(args.Type).Append(", ");
        sb.Append(args.Year).Append(", ");
        sb.Append("static () => (global::Hawkynt.ColorProcessing.IDitherer)(object)new ").Append(typeRef).Append("()");
        sb.AppendLine("));");
      }
    }

    sb.AppendLine("  }");
    sb.AppendLine("}");
    ctx.AddSource("DithererRegistry.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
  }

  public static void EmitQuantizers(SourceProductionContext ctx, ImmutableArray<RegistryCandidate> items) {
    var ordered = _Order(items);
    var sb = new StringBuilder(Header);
    sb.AppendLine("using System;");
    sb.AppendLine("using System.Collections.Generic;");
    sb.AppendLine("using Hawkynt.ColorProcessing;");
    sb.AppendLine("using Hawkynt.ColorProcessing.Quantization;");
    sb.AppendLine();
    sb.AppendLine("namespace Hawkynt.ColorProcessing.Quantization;");
    sb.AppendLine();
    sb.AppendLine("partial class QuantizerRegistry {");
    sb.AppendLine("  static partial void _CollectFromSourceGenerator(global::System.Collections.Generic.List<global::Hawkynt.ColorProcessing.Quantization.QuantizerDescriptor> into) {");

    foreach (var c in ordered) {
