  private static IEnumerable<RegistryCandidate> _Order(ImmutableArray<RegistryCandidate> items)
    => items.OrderBy(i => i.FileHint, System.StringComparer.Ordinal).ThenBy(i => i.SyntaxOrder);

  private static string _SimpleTypeName(string fqn) {
    var idx = fqn.LastIndexOf('.');
    return idx < 0 ? fqn : fqn.Substring(idx + 1);
  }

  // --- Attribute argument resolution ---------------------------------------------------------

  private readonly record struct ScalerArgs(string Name, string Author, string Description, string Url, string Year, string Category);
  private readonly record struct FilterArgs(string Name, string Author, string Description, string Url, string Year, string Category);
  private readonly record struct DithererArgs(string Name, string? NameRaw, string Author, string Description, string Type, string Year);
  private readonly record struct QuantizerArgs(string DisplayName, string? DisplayNameRaw, string Author, string Year, string QualityRating, string QuantizationType);

  private static ScalerArgs _ResolveScalerAttributeArgs(RegistryCandidate c) {
    // ScalerInfoAttribute(string name) + named props: Author, Url, Year, Description, Category
    var ctor = c.Attribute.ConstructorArguments;
    var name = ctor.Count > 0 ? ctor[0].SourceLiteral : "\"\"";
    var named = _NamedLookup(c.Attribute.NamedArguments);
    return new ScalerArgs(
      Name: name,
      Author: named.TryGetValue("Author", out var a) ? a.SourceLiteral : "null",
      Description: named.TryGetValue("Description", out var d) ? d.SourceLiteral : "null",
      Url: named.TryGetValue("Url", out var u) ? u.SourceLiteral : "null",
      Year: named.TryGetValue("Year", out var y) ? y.SourceLiteral : "0",
      Category: named.TryGetValue("Category", out var cat) ? cat.SourceLiteral : "global::Hawkynt.ColorProcessing.Resizing.ScalerCategory.Rescaler"
    );
  }

  private static FilterArgs _ResolveFilterAttributeArgs(RegistryCandidate c) {
    var ctor = c.Attribute.ConstructorArguments;
    var name = ctor.Count > 0 ? ctor[0].SourceLiteral : "\"\"";
    var named = _NamedLookup(c.Attribute.NamedArguments);
    return new FilterArgs(
      Name: name,
      Author: named.TryGetValue("Author", out var a) ? a.SourceLiteral : "null",
      Description: named.TryGetValue("Description", out var d) ? d.SourceLiteral : "null",
      Url: named.TryGetValue("Url", out var u) ? u.SourceLiteral : "null",
      Year: named.TryGetValue("Year", out var y) ? y.SourceLiteral : "0",
      Category: named.TryGetValue("Category", out var cat) ? cat.SourceLiteral : "global::Hawkynt.ColorProcessing.Filtering.FilterCategory.Enhancement"
    );
  }

  private static DithererArgs _ResolveDithererAttributeArgs(RegistryCandidate c) {
    var ctor = c.Attribute.ConstructorArguments;
    var nameTv = ctor.Count > 0 ? ctor[0] : null;
    var named = _NamedLookup(c.Attribute.NamedArguments);
    return new DithererArgs(
      Name: nameTv?.SourceLiteral ?? "null",
      NameRaw: nameTv?.RawString,
      Author: named.TryGetValue("Author", out var a) ? a.SourceLiteral : "null",
      Description: named.TryGetValue("Description", out var d) ? d.SourceLiteral : "null",
      Type: named.TryGetValue("Type", out var t) ? t.SourceLiteral : "global::Hawkynt.ColorProcessing.Dithering.DitheringType.Custom",
      Year: named.TryGetValue("Year", out var y) ? y.SourceLiteral : "0"
    );
  }

  private static QuantizerArgs _ResolveQuantizerAttributeArgs(RegistryCandidate c) {
    var ctor = c.Attribute.ConstructorArguments;
    var qt = ctor.Count > 0 ? ctor[0].SourceLiteral : "global::Hawkynt.ColorProcessing.Quantization.QuantizationType.Tree";
    var named = _NamedLookup(c.Attribute.NamedArguments);
    var dispName = named.TryGetValue("DisplayName", out var dn) ? dn : null;
    return new QuantizerArgs(
      DisplayName: dispName?.SourceLiteral ?? "null",
      DisplayNameRaw: dispName?.RawString,
      Author: named.TryGetValue("Author", out var a) ? a.SourceLiteral : "null",
      Year: named.TryGetValue("Year", out var y) ? y.SourceLiteral : "0",
      QualityRating: named.TryGetValue("QualityRating", out var qr) ? qr.SourceLiteral : "0",
      QuantizationType: qt
    );
  }

  private static Dictionary<string, TypedValue> _NamedLookup(EquatableArray<NamedArg> items) {
    var d = new Dictionary<string, TypedValue>(System.StringComparer.Ordinal);
    foreach (var na in items)
      d[na.Name] = na.Value;
    return d;
  }
}
