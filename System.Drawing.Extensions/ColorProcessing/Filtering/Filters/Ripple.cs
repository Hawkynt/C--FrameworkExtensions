namespace Hawkynt.ColorProcessing.Filtering.Filters;

/// <summary>
/// Ripple distortion â€” displaces pixels along a sinusoidal wave perpendicular to a given angle.
/// </summary>
[FilterInfo("Ripple",
  Description = "Sinusoidal displacement perpendicular to angle direction", Category = FilterCategory.Distortion)]
public readonly struct Ripple(float amplitude, float wavelength = 20f, float angle = 0f) : IPixelFilter, IFrameFilter {
  private readonly float _amplitude = Math.Max(0f, amplitude);
  private readonly float _wavelength = Math.Max(1f, wavelength);
  private readonly float _angle = angle;

  public Ripple() : this(5f, 20f, 0f) { }

  /// <inheritdoc />
  public bool UsesFrameAccess => true;

  /// <inheritdoc />
  public TResult InvokeKernel<TWork, TKey, TPixel, TDistance, TEquality, TLerp, TEncode, TResult>(
    IKernelCallback<TWork, TKey, TPixel, TEncode, TResult> callback,
    TEquality equality = default,
    TLerp lerp = default)
    where TWork : unmanaged, IColorSpace
    where TKey : unmanaged, IColorSpace
    where TPixel : unmanaged, IStorageSpace
    where TDistance : struct, IColorMetric<TKey>, INormalizedMetric
    where TEquality : struct, IColorEquality<TKey>
    where TLerp : struct, ILerp<TWork>
    where TEncode : struct, IEncode<TWork, TPixel>
    => callback.Invoke(new RipplePassThroughKernel<TWork, TKey, TPixel, TEncode>());

  /// <inheritdoc />
  public TResult InvokeFrameKernel<TWork, TKey, TPixel, TDecode, TProject, TEncode, TResult>(
    IResampleKernelCallback<TWork, TKey, TPixel, TDecode, TProject, TEncode, TResult> callback,
    int sourceWidth, int sourceHeight)
    where TWork : unmanaged, IColorSpace4F<TWork>
    where TKey : unmanaged, IColorSpace
    where TPixel : unmanaged, IStorageSpace
    where TDecode : struct, IDecode<TPixel, TWork>
    where TProject : struct, IProject<TWork, TKey>
    where TEncode : struct, IEncode<TWork, TPixel>
    => callback.Invoke(new RippleFrameKernel<TPixel, TWork, TKey, TDecode, TProject, TEncode>(
      this._amplitude, this._wavelength, this._angle, sourceWidth, sourceHeight));

  public static Ripple Default => new();
}

file readonly struct RipplePassThroughKernel<TWork, TKey, TPixel, TEncode>
  : IScaler<TWork, TKey, TPixel, TEncode>
  where TWork : unmanaged, IColorSpace
  where TKey : unmanaged, IColorSpace
  where TPixel : unmanaged, IStorageSpace
  where TEncode : struct, IEncode<TWork, TPixel> {

  public int ScaleX => 1;
  public int ScaleY => 1;

  [MethodImpl(MethodImplOptions.AggressiveInlining)]
  public unsafe void Scale(
    in NeighborWindow<TWork, TKey> window,
    TPixel* dest,
    int destStride,
    in TEncode encoder)
    => dest[0] = encoder.Encode(window.P0P0.Work);
}

file readonly struct RippleFrameKernel<TPixel, TWork, TKey, TDecode, TProject, TEncode>(
  float amplitude, float wavelength, float angle, int sourceWidth, int sourceHeight)
  : IResampleKernel<TPixel, TWork, TKey, TDecode, TProject, TEncode>
  where TPixel : unmanaged, IStorageSpace
  where TWork : unmanaged, IColorSpace4F<TWork>
  where TKey : unmanaged, IColorSpace
  where TDecode : struct, IDecode<TPixel, TWork>
  where TProject : struct, IProject<TWork, TKey>
  where TEncode : struct, IEncode<TWork, TPixel> {

  public int Radius => (int)Math.Ceiling(amplitude) + 1;
  public int SourceWidth => sourceWidth;
  public int SourceHeight => sourceHeight;
  public int TargetWidth => sourceWidth;
  public int TargetHeight => sourceHeight;

  [MethodImpl(MethodImplOptions.AggressiveInlining)]
  public unsafe void Resample(
    NeighborFrame<TPixel, TWork, TKey, TDecode, TProject> frame,
    int destX, int destY,
    TPixel* dest, int destStride,
    in TEncode encoder) {
    var rad = angle * (float)(Math.PI / 180.0);
    var cos = (float)Math.Cos(rad);
    var sin = (float)Math.Sin(rad);

    // Project position along angle direction
    var along = destX * cos + destY * sin;
    var displacement = amplitude * (float)Math.Sin(2.0 * Math.PI * along / wavelength);

    // Displace perpendicular to angle
    var sx = (int)(destX - sin * displacement);
    var sy = (int)(destY + cos * displacement);

    var px = frame[sx, sy].Work;
    var (r, g, b, a) = ColorConverter.GetNormalizedRgba(in px);
    dest[destY * destStride + destX] = encoder.Encode(ColorConverter.FromNormalizedRgba<TWork>(r, g, b, a));
  }
}
