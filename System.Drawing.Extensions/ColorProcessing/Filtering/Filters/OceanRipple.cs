/// <summary>
/// OceanRipple distortion â€” applies dual-axis sinusoidal displacement simulating ocean surface ripples.
/// </summary>
[FilterInfo("OceanRipple",
  Description = "Dual-axis sinusoidal ocean ripple distortion", Category = FilterCategory.Distortion)]
public readonly struct OceanRipple(float amplitudeX = 5f, float amplitudeY = 5f, int sizeX = 15, int sizeY = 15) : IPixelFilter, IFrameFilter {
  private readonly float _amplitudeX = amplitudeX;
  private readonly float _amplitudeY = amplitudeY;
  private readonly int _sizeX = Math.Max(1, sizeX);
  private readonly int _sizeY = Math.Max(1, sizeY);

  /// <inheritdoc />
  public bool UsesFrameAccess => true;

  /// <inheritdoc />
  public TResult InvokeKernel<TWork, TKey, TPixel, TDistance, TEquality, TLerp, TEncode, TResult>(
    IKernelCallback<TWork, TKey, TPixel, TEncode, TResult> callback,
    TEquality equality = default,
    TLerp lerp = default)
    where TWork : unmanaged, IColorSpace
    where TKey : unmanaged, IColorSpace
    where TPixel : unmanaged, IStorageSpace
    where TDistance : struct, IColorMetric<TKey>, INormalizedMetric
    where TEquality : struct, IColorEquality<TKey>
    where TLerp : struct, ILerp<TWork>
    where TEncode : struct, IEncode<TWork, TPixel>
    => callback.Invoke(new OceanRipplePassThroughKernel<TWork, TKey, TPixel, TEncode>());

  /// <inheritdoc />
  public TResult InvokeFrameKernel<TWork, TKey, TPixel, TDecode, TProject, TEncode, TResult>(
    IResampleKernelCallback<TWork, TKey, TPixel, TDecode, TProject, TEncode, TResult> callback,
    int sourceWidth, int sourceHeight)
    where TWork : unmanaged, IColorSpace4F<TWork>
    where TKey : unmanaged, IColorSpace
    where TPixel : unmanaged, IStorageSpace
    where TDecode : struct, IDecode<TPixel, TWork>
    where TProject : struct, IProject<TWork, TKey>
    where TEncode : struct, IEncode<TWork, TPixel>
    => callback.Invoke(new OceanRippleFrameKernel<TPixel, TWork, TKey, TDecode, TProject, TEncode>(
      this._amplitudeX, this._amplitudeY, this._sizeX, this._sizeY, sourceWidth, sourceHeight));

  public static OceanRipple Default => new(5f, 5f, 15, 15);
}

file readonly struct OceanRipplePassThroughKernel<TWork, TKey, TPixel, TEncode>
  : IScaler<TWork, TKey, TPixel, TEncode>
  where TWork : unmanaged, IColorSpace
  where TKey : unmanaged, IColorSpace
  where TPixel : unmanaged, IStorageSpace
  where TEncode : struct, IEncode<TWork, TPixel> {

  public int ScaleX => 1;
  public int ScaleY => 1;

  [MethodImpl(MethodImplOptions.AggressiveInlining)]
  public unsafe void Scale(
    in NeighborWindow<TWork, TKey> window,
    TPixel* dest,
    int destStride,
    in TEncode encoder)
    => dest[0] = encoder.Encode(window.P0P0.Work);
}

file readonly struct OceanRippleFrameKernel<TPixel, TWork, TKey, TDecode, TProject, TEncode>(
  float amplitudeX, float amplitudeY, int sizeX, int sizeY, int sourceWidth, int sourceHeight)
  : IResampleKernel<TPixel, TWork, TKey, TDecode, TProject, TEncode>
  where TPixel : unmanaged, IStorageSpace
  where TWork : unmanaged, IColorSpace4F<TWork>
  where TKey : unmanaged, IColorSpace
  where TDecode : struct, IDecode<TPixel, TWork>
  where TProject : struct, IProject<TWork, TKey>
  where TEncode : struct, IEncode<TWork, TPixel> {

  public int Radius => (int)Math.Ceiling(Math.Max(Math.Abs(amplitudeX), Math.Abs(amplitudeY))) + 1;
  public int SourceWidth => sourceWidth;
  public int SourceHeight => sourceHeight;
  public int TargetWidth => sourceWidth;
  public int TargetHeight => sourceHeight;

  [MethodImpl(MethodImplOptions.AggressiveInlining)]
  public unsafe void Resample(
    NeighborFrame<TPixel, TWork, TKey, TDecode, TProject> frame,
    int destX, int destY,
    TPixel* dest, int destStride,
    in TEncode encoder) {
    var sx = (int)(destX + amplitudeX * (float)Math.Sin(2.0 * Math.PI * destY / Math.Max(1, sizeY)));
    var sy = (int)(destY + amplitudeY * (float)Math.Sin(2.0 * Math.PI * destX / Math.Max(1, sizeX)));

    var px = frame[sx, sy].Work;
    var (r, g, b, a) = ColorConverter.GetNormalizedRgba(in px);
    dest[destY * destStride + destX] = encoder.Encode(ColorConverter.FromNormalizedRgba<TWork>(r, g, b, a));
  }
}
